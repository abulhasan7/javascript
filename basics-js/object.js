//{}is called as object notation
let object1 = {
    name:'Abul Hasan',
    age: 25
};
console.log(object1)
//dot notation, keep dot as go-to notation
object1.name = 'Mohammed';
console.log(object1)

//bracket notation, will be useful when we don't know the property yet
object1['name'] = 'Abul Hasan';
console.log(object1)
